    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (checkEmptyTxt() == true)
            return;
        else {
            NhanVienBUS bus = new NhanVienBUS();

            if (bus.kiemTraTrung(txtMaNV.getText())) {
                JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại. Mời nhập Mã nhân viên khác!");
                return;
            }

            NhanVienDTO nv = new NhanVienDTO();
            Date date = DCNgaySinh.getDate();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String strDate = df.format(date);
            nv.setNgaySinh(strDate);
            
            nv.setMaNV(txtMaNV.getText());
            nv.setHo(txtHo.getText());
            nv.setTen(txtTen.getText());
            if(btnNam.isSelected())
            {
                nv.setGioiTinh(btnNam.getText());
            }
            if(btnNu.isSelected())
            {
                nv.setGioiTinh(btnNu.getText());
            }

            nv.setDiaChi(txtDiaChi.getText());
            nv.setLuong(Float.valueOf(txtLuong.getText()));

            try {
                bus.them(nv);
            } catch (Exception ex) {
                Logger.getLogger(NhanVienFRM.class.getName()).log(Level.SEVERE, null, ex);
            }

            String[] columnNames = {"Mã nhân viên", "Họ", "Tên", "Giới tính", "Ngày sinh", "Địa chỉ", "Lương"};
            Vector header = createHeader(columnNames);

            if (model.getRowCount() == 0) {
                model = new DefaultTableModel(header, 0);
            }

            Vector row = new Vector();
            row.add(nv.getMaNV());
            row.add(nv.getHo());
            row.add(nv.getTen());
            row.add(nv.getGioiTinh());
            row.add(nv.getNgaySinh());
            row.add(nv.getDiaChi());
            row.add(nv.getLuong());

            model.addRow(row);
            tblNhanVien.setModel(model);

            String [] arrayNV = new String[NhanVienBUS.getDsnv().size()];
                try {
                  arrayNV = bus.docComboBoxMaNV();
                } catch (Exception ex) {
                    Logger.getLogger(NhanVienFRM.class.getName()).log(Level.SEVERE, null, ex);
                }
            try {
                pn.docComboBoxNV(arrayNV);
                tk.docComboBoxNV(arrayNV);               
                hd.docComboBoxNV(arrayNV);
            } catch (Exception ex) {
                Logger.getLogger(NhanVienFRM.class.getName()).log(Level.SEVERE, null, ex);
            }             
           
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            try {
                resetTxt();
            } catch (Exception ex) {
                Logger.getLogger(NhanVienFRM.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

