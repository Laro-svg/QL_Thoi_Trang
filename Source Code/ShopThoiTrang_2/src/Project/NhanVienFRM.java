    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String mucTimKiem = comboBoxMucTimKiem.getSelectedItem().toString();
        String duLieu = txtTimKiem.getText();

        String[] columnNames = {"Mã nhân viên", "Họ", "Tên", "Giới tính","Ngày sinh", "Địa chỉ", "Lương"};
        Vector header = createHeader(columnNames);

        if (duLieu.equals("") == false) {
            ArrayList<NhanVienDTO> tempArr = new ArrayList<NhanVienDTO>();
            NhanVienBUS bus = new NhanVienBUS();

            tempArr = bus.timKiem(mucTimKiem, duLieu);

            if (tempArr.size() > 0) {
                modelTimKiem = new DefaultTableModel();

                if (modelTimKiem.getRowCount() == 0) {
                    modelTimKiem = new DefaultTableModel(header, 0);
                }

                for (NhanVienDTO nv : tempArr) {
                    Vector row = new Vector();
                    row.add(nv.getMaNV());
                    row.add(nv.getHo());
                    row.add(nv.getTen());
                    row.add(nv.getGioiTinh());
                    row.add(nv.getNgaySinh());
                    row.add(nv.getDiaChi());
                    row.add(nv.getLuong());

                    modelTimKiem.addRow(row);
                }
                tblNhanVien.setModel(modelTimKiem);
                tblNhanVien = initHeaderColor(tblNhanVien, 14, 142, 233);

                flag = false;
                anCacNutChucNang();

            } else {
                JOptionPane.showMessageDialog(this, "Rất tiếc. Dữ liệu không được tìm thấy!");
            }
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

