    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        int i = tblKhuyenMaiHopLe.getSelectedRow();
        if (i >= 0)
        {
            String maKM = model.getValueAt(i, 0).toString();
            hd.setTxtMaKM(maKM);
            try {
                hd.resetTxtMoney(maHD, maKM);
            } catch (Exception ex) {
                Logger.getLogger(KhuyenMaiHopLeFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        }
        else
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn Mã KM!");
    }//GEN-LAST:event_btnOKActionPerformed
    
       
