    public Vector createHeader(Object[] columnNames) {
        Vector header = new Vector();
        for (Object colName : columnNames) {
            header.add(colName);
        }

        return header;
    }
    
    public void locKhuyenMai(String maHD) throws Exception {
        KhuyenMaiHopLeBUS bus = new KhuyenMaiHopLeBUS();
        bus.docDSKMHL();
        
        this.maHD = maHD;

        String[] columnNames = {"Mã KM", "Tên KM"};
        Vector header = createHeader(columnNames);

        if (model.getRowCount() == 0) {
            model = new DefaultTableModel(header, 0);
        }

        for (KhuyenMaiHopLeDTO kmhl : bus.timDSKMHL(maHD)) {
            Vector row = new Vector();
            row.add(kmhl.getMaKM());
            row.add(kmhl.getTenKM());

            model.addRow(row);
        }

        tblKhuyenMaiHopLe.setModel(model);
        tblKhuyenMaiHopLe = initHeaderColor(tblKhuyenMaiHopLe, 14, 142, 233);
    }
    
