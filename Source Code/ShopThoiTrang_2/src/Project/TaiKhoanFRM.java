    public Vector createHeader(Object[] columnNames) {
        Vector header = new Vector();
        for (Object colName : columnNames) {
            header.add(colName);
        }

        return header;
    }


        
    public void docDatabase() throws Exception {
        TaiKhoanBUS bus = new TaiKhoanBUS();
        if (TaiKhoanBUS.getDstk() == null) {
            bus.docDSTK();
        }

        String[] columnNames = {"Mã NV", "Tên đăng nhập", "Mật khẩu", "Quyền"};
        Vector header = createHeader(columnNames);

        if (model.getRowCount() == 0) {
            model = new DefaultTableModel(header, 0);
        }

        for (TaiKhoanDTO tk : TaiKhoanBUS.getDstk()) {
            Vector row = new Vector();
            row.add(tk.getMaNV());
            row.add(tk.getTenDangNhap());
            row.add(tk.getMatKhau());
            row.add(tk.getQuyen());

            model.addRow(row);
        }

        tblTaiKhoan.setModel(model);
        tblTaiKhoan = initHeaderColor(tblTaiKhoan, 14, 142, 233);

    }

    public void resetTxt() throws Exception {
        txtTenDangNhap.setText("");
        txtMatKhau.setText("");
        comboBoxQuyen.setSelectedItem("Nhân viên");
    }

    public boolean checkEmptyTxt() {
        if (txtTenDangNhap.getText().equals("") || txtMatKhau.getText().equals("")) {
            return true;
        }
        return false;
    }

