    private void tblTaiKhoanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTaiKhoanMouseClicked
        int i = tblTaiKhoan.getSelectedRow();
        if (i >= 0) {
            TaiKhoanBUS bus = new TaiKhoanBUS();
            TaiKhoanDTO tk = new TaiKhoanDTO();
            if (flag == true) {
                tk = TaiKhoanBUS.getDstk().get(i);
            } else {
                int index = bus.timIndex(modelTimKiem.getValueAt(i, 0).toString());
                tk = TaiKhoanBUS.getDstk().get(index);
            }

            comboBoxTaiKhoan.setSelectedItem(tk.getMaNV());
            txtTenDangNhap.setText(tk.getTenDangNhap());
            txtMatKhau.setText(tk.getMatKhau());
            comboBoxQuyen.setSelectedItem(tk.getQuyen());

            btnXoa.setEnabled(true);
            btnSua.setEnabled(true);
        }
    }//GEN-LAST:event_tblTaiKhoanMouseClicked

